function talkButton() {
    const btn = document.getElementById('talkBtn');
    const target = document.getElementById('popOut');
    const mail = "{{asset('backend/img/icon_contact-mail.png')}}";
    let num = 0;
    let count = 0;
    let max = 19;
    let min = 1;

    function addImg(){
        num = Math.floor(Math.random() * (max - min) + min);
        count += 1;
        const img = '<img src="' + mail + '" class="c-popMail c-popMail-' + num + ' jsMail-' + count +'" alt="">';
        target.insertAdjacentHTML('afterbegin', img);

        const removeElem = '.jsMail-' + count
        const removeImg = document.querySelector(removeElem);

        setTimeout(()=>{
            removeImg.remove();
        }, 3000);
    }

    btn.addEventListener('mouseover',()=>{
        addImg();
    });
}
talkButton();
